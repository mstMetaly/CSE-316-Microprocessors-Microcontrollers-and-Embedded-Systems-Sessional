.MODEL SMALL
.STACK 100H


.DATA
BYTE1 DB 0FAH
BYTE2 DB 0AEH

.CODE

MAIN  PROC 
    
	MOV 	AX,3
	PUSH 	AX
	CALL 	FACTORIAL
	MOV 	AH,4CH
	INT 	21H 
	
MAIN ENDP
    	      
    	      
FACTORIAL PROC NEAR 
    
	PUSH 	BP
	MOV 	BP,SP
	CMP 	WORD PTR[BP+4],1
	JG  	END_IF
	MOV 	AX,1
	JMP 	RETURN 
	
END_IF:	MOV	CX, [BP+4]
	DEC 	CX
	PUSH	CX
	CALL 	FACTORIAL
	MUL 	WORD  PTR[BP+4] 
	
RETURN:	
    POP	BP
	RET	2 
	
END	MAIN
